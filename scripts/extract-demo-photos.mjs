import { readFile, writeFile } from 'node:fs/promises'
import { resolve } from 'node:path'

const [, , backupPath] = process.argv

const selections = [
  { sourceIndex: 0, id: 'demo-brew-0', type: 'coffee' },
  { sourceIndex: 1, id: 'demo-brew-1', type: 'coffee' },
  { sourceIndex: 7, id: 'demo-brew-2', type: 'matcha' },
]

async function main() {
  if (!backupPath) {
    throw new Error('Usage: node scripts/extract-demo-photos.mjs <path-to-backup.json>')
  }

  let backup
  try {
    backup = JSON.parse(await readFile(resolve(backupPath), 'utf8'))
  } catch (error) {
    throw new Error(`Could not read backup JSON: ${error.message}`)
  }

  if (!Array.isArray(backup)) {
    throw new Error('Backup JSON must contain an array of entries')
  }

  const specs = selections.map(({ sourceIndex, id, type }) => {
    const source = backup[sourceIndex]
    if (!source || typeof source.photo !== 'string') {
      throw new Error(`Backup entry at index ${sourceIndex} is missing a photo`) 
    }

    return {
      id,
      type,
      name: source.name,
      color: source.color ?? null,
      rating: source.rating ?? null,
      photo: source.photo,
    }
  })

  const output = `// Generated by scripts/extract-demo-photos.mjs. Do not edit by hand.\nexport const DEMO_SPECS = ${JSON.stringify(specs, null, 2)}\n`
  const outputPath = new URL('../src/lib/demoData.js', import.meta.url)
  await writeFile(outputPath, output)
  console.log(`Wrote ${specs.length} demo photo specs to ${outputPath.pathname}`)
}

main().catch(error => {
  console.error(error.message)
  process.exitCode = 1
})
